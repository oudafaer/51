void main(void)
{
	uchar i , j;
	uint n;
	Lcd12864_Init();        //初始化
	Lcd12864_ClearScreen();	//清屏

	for (i=0;i<8;i++)
	{  
	    //--设置初始显示开始位置--//
		LcdSt7565_WriteCmd(0x40);

		//--设置Y坐标--//
		LcdSt7565_WriteCmd(0xB0+i);
		
		//--设置X坐标--// 
		LcdSt7565_WriteCmd(0x10); 
		LcdSt7565_WriteCmd(0x04); 
		for(j=0; j<128; j++)
		{
			LcdSt7565_WriteData(~pic[n]);
			n=n+1;
		}
  }
	while (1)
	{
	}
}
